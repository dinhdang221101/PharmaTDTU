package modal;

/**
 *
 * @author DinhDang
 */
public class Thuoc {
    private String id;
    private String name;
    private String price;
    private String description;
    private String image;

    public Thuoc() {
    }

    public Thuoc(String id, String name, String price, String description, String image) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.description = description;
        this.image = image;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPrice() {
        return price;
    }

    public String getDescription() {
        return description;
    }

    public String getImage() {
        return image;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public void setImage(String image) {
        this.image = image;
    }

    @Override
    public String toString() {
        return "Thuoc{" + "id=" + id + ", name=" + name + ", price=" + price + ", description=" + description + ", image=" + image + '}';
    }

    
}
